/**
 * アイコンを取得する関数
 * @param {string} name - アイコン名
 * @param {string} className - 追加するCSSクラス（オプション）
 * @returns {string} - SVG HTML
 */
function getIcon(name, className = '') {
    const svg = ICONS[name] || ICONS.info; // デフォルトはinfoアイコン

    if (className) {
        return svg.replace('<svg', `<svg class="${className}"`);
    }

    return svg;
}

/**
 * ボタン付きアイコンを生成
 * ⚠️ セキュリティ上の理由により、onclickパラメータは廃止されました
 * イベントリスナーは呼び出し元でaddEventListenerを使用してください
 *
 * @param {string} iconName - アイコン名
 * @param {string} buttonClass - ボタンのCSSクラス
 * @param {string} dataAction - data-action属性の値（オプション）
 * @param {string} title - ツールチップテキスト
 * @returns {string} - ボタンHTML
 */
function iconButton(iconName, buttonClass = 'btn-icon', dataAction = '', title = '') {
    const icon = getIcon(iconName);
    const titleAttr = title ? `title="${title}"` : '';
    const dataAttr = dataAction ? `data-action="${dataAction}"` : '';

    return `<button class="${buttonClass}" ${dataAttr} ${titleAttr}>${icon}</button>`;
}

